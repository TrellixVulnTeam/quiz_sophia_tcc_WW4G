    def get_nivel_de_dificuldade(self):
        return self.nivel_de_dificuldade

    def get_nivel_de_aprendizado(self):
        return self.nivel_de_aprendizado

    def get_sentenca(self):
        return self.sentenca

    def get_percentual(self):
        return self.percentual